package com.bootcoding.leetcode.Leetcode;
class TreeNode {
    // Data stored in the tree node
    int data; // Or any other data type based on your requirement
    // References to child nodes
    TreeNode left; // Reference to the left child node
    TreeNode right; // Reference to the right child node

    // Constructor to initialize the tree node with data
    public TreeNode(int data) {
        this.data = data;
        this.left = null;
        this.right = null;
    }
}


